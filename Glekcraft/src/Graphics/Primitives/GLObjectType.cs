namespace Glekcraft.Graphics.Primitives;

/// <summary>
/// An enumeration of types of OpenGL objects.
/// </summary>
public enum GLObjectType {
    /// <summary>
    /// An OpenGL buffer.
    /// </summary>
    Buffer,

    /// <summary>
    /// An OpenGL shader object.
    /// </summary>
    Shader,

    /// <summary>
    /// An OpenGL shader program object.
    /// </summary>
    ShaderProgram,

    /// <summary>
    /// An OpenGL texture object.
    /// </summary>
    Texture
}
